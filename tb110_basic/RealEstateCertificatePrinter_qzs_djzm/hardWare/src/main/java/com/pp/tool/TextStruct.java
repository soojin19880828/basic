package com.pp.tool;

public class TextStruct {
	public String text;
	public float x;
	public float y;
	public Integer fontsize;
	public float maxwidth;

	public TextStruct(String text, float x, float y, Integer fontsize, float maxwidth) {
		this.text = text;
		this.x = x;
		this.y = y;
		this.fontsize = fontsize;
		this.maxwidth = maxwidth;
	}

}
